static void i2c_init(void)
{
    i2c_config_t conf = {
        .mode = I2C_MODE_MASTER,
        .sda_io_num = IIC0_SDA_GPIO_PIN,
        .scl_io_num = IIC0_SCL_GPIO_PIN,
        .sda_pullup_en = GPIO_PULLUP_ENABLE,
        .scl_pullup_en = GPIO_PULLUP_ENABLE,
        .master.clk_speed = IIC_FREQ,
    };
    i2c_param_config(I2C_NUM, &conf);
    i2c_driver_install(I2C_NUM, I2C_MODE_MASTER, 0, 0, 0);
}

static void xl9555_write_byte(uint8_t reg, uint8_t *data, size_t len)
{
    i2c_cmd_handle_t cmd = i2c_cmd_link_create();
    i2c_master_start(cmd);
    i2c_master_write_byte(cmd, XL9555_ADDR << 1, true);
    i2c_master_write_byte(cmd, reg, true);
    for (size_t i = 0; i < len; i++) {
        i2c_master_write_byte(cmd, data[i], true);
    }
    i2c_master_stop(cmd);
    i2c_master_cmd_begin(I2C_NUM, cmd, 100 / portTICK_PERIOD_MS);
    i2c_cmd_link_delete(cmd);
}

static void xl9555_pin_write(uint16_t pin, int val)
{
    uint8_t w_data[2] = {0};
    
    i2c_cmd_handle_t cmd = i2c_cmd_link_create();
    i2c_master_start(cmd);
    i2c_master_write_byte(cmd, XL9555_ADDR << 1, true);
    i2c_master_write_byte(cmd, XL9555_OUTPUT_PORT0_REG, true);
    i2c_master_stop(cmd);
    i2c_master_cmd_begin(I2C_NUM, cmd, 100 / portTICK_PERIOD_MS);
    i2c_cmd_link_delete(cmd);

    cmd = i2c_cmd_link_create();
    i2c_master_start(cmd);
    i2c_master_write_byte(cmd, (XL9555_ADDR << 1) | 1, true);
    i2c_master_read(cmd, w_data, 2, I2C_MASTER_LAST_NACK);
    i2c_master_stop(cmd);
    i2c_master_cmd_begin(I2C_NUM, cmd, 100 / portTICK_PERIOD_MS);
    i2c_cmd_link_delete(cmd);

    if (pin <= 0x00FF) {
        if (val) {
            w_data[0] |= (uint8_t)(0xFF & pin);
        } else {
            w_data[0] &= ~(uint8_t)(0xFF & pin);
        }
    } else {
        if (val) {
            w_data[1] |= (uint8_t)(0xFF & (pin >> 8));
        } else {
            w_data[1] &= ~(uint8_t)(0xFF & (pin >> 8));
        }
    }

    xl9555_write_byte(XL9555_OUTPUT_PORT0_REG, w_data, 2);
}

static void lcd_hard_reset(void)
{
    gpio_set_level(EXAMPLE_PIN_NUM_LCD_RST, 0);
    vTaskDelay(pdMS_TO_TICKS(100));
    gpio_set_level(EXAMPLE_PIN_NUM_LCD_RST, 1);
    vTaskDelay(pdMS_TO_TICKS(100));
}

static void lcd_write_cmd(uint8_t cmd)
{
    gpio_set_level(EXAMPLE_PIN_NUM_LCD_DC, 0);
    spi_transaction_t t = {0};
    t.length = 8;
    t.tx_buffer = &cmd;
    spi_device_polling_transmit(lcd_spi_handle, &t);
}

static void lcd_write_data(const uint8_t *data, int len)
{
    gpio_set_level(EXAMPLE_PIN_NUM_LCD_DC, 1);
    spi_transaction_t t = {0};
    t.length = len * 8;
    t.tx_buffer = data;
    spi_device_polling_transmit(lcd_spi_handle, &t);
}

static void lcd_write_data16(uint16_t data)
{
    uint8_t data_buf[2] = {0, 0};
    data_buf[0] = data >> 8;
    data_buf[1] = data & 0xFF;
    lcd_write_data(data_buf, 2);
}

static void lcd_set_window(uint16_t xstar, uint16_t ystar, uint16_t xend, uint16_t yend)
{
    uint8_t databuf[4] = {0, 0, 0, 0};
    databuf[0] = xstar >> 8;
    databuf[1] = xstar & 0xFF;
    databuf[2] = xend >> 8;
    databuf[3] = xend & 0xFF;
    lcd_write_cmd(0x2A);
    lcd_write_data(databuf, 4);

    databuf[0] = ystar >> 8;
    databuf[1] = ystar & 0xFF;
    databuf[2] = yend >> 8;
    databuf[3] = yend & 0xFF;
    lcd_write_cmd(0x2B);
    lcd_write_data(databuf, 4);

    lcd_write_cmd(0x2C);
}

static void lcd_fill(uint16_t sx, uint16_t sy, uint16_t ex, uint16_t ey, uint16_t color)
{
    uint16_t width = ex - sx + 1;
    uint16_t height = ey - sy + 1;
    lcd_set_window(sx, sy, ex, ey);

    uint8_t data_buf[2] = {0};
    data_buf[0] = color >> 8;
    data_buf[1] = color & 0xFF;

    for (uint16_t i = 0; i < height; i++) {
        for (uint16_t j = 0; j < width; j++) {
            lcd_write_data(data_buf, 2);
        }
    }
}

static void lcd_clear(uint16_t color)
{
    lcd_fill(0, 0, LCD_WIDTH - 1, LCD_HEIGHT - 1, color);
}

