static void lcd_show_char(uint16_t x, uint16_t y, uint8_t chr, uint8_t size, uint16_t color)
{
    uint8_t temp = 0, t1 = 0;
    uint8_t *pfont = 0;
    uint8_t csize = 0;
    uint16_t colortemp = 0;

    csize = (size / 8 + ((size % 8) ? 1 : 0)) * (size / 2);
    chr = chr - ' ';

    if ((x > (LCD_WIDTH - size / 2)) || (y > (LCD_HEIGHT - size))) {
        return;
    }

    lcd_set_window(x, y, x + size / 2 - 1, y + size - 1);

    switch (size) {
        case 16:
            pfont = (uint8_t *)asc2_1608[chr];
            break;
        default:
            return;
    }

    csize = (size / 8 + ((size % 8) ? 1 : 0)) * (size / 2);

    for (uint8_t t = 0; t < csize; t++) {
        temp = pfont[t];
        for (t1 = 0; t1 < 8; t1++) {
            if (temp & 0x80) {
                colortemp = color;
            } else {
                colortemp = BLACK;
            }
            lcd_write_data16(colortemp);
            temp <<= 1;
        }
    }
}

static void lcd_show_string(uint16_t x, uint16_t y, uint8_t size, char *p, uint16_t color)
{
    uint8_t x0 = x;
    while ((*p <= '~') && (*p >= ' ')) {
        if (x >= LCD_WIDTH) {
            x = x0;
            y += size;
        }
        if (y >= LCD_HEIGHT) break;
        lcd_show_char(x, y, *p, size, color);
        x += size / 2;
        p++;
    }
}

static int lcd_find_chinese_char(uint32_t code)
{
    int low = 0, high = LONGYIN_CHAR_COUNT - 1;
    while (low <= high) {
        int mid = (low + high) / 2;
        if (longyin_char_codes[mid] == code) {
            return mid;
        } else if (longyin_char_codes[mid] < code) {
            low = mid + 1;
        } else {
            high = mid - 1;
        }
    }
    return -1;
}

static void lcd_show_chinese_char(uint16_t x, uint16_t y, uint32_t code, uint16_t color)
{
    int index = lcd_find_chinese_char(code);
    if (index < 0) return;

    lcd_set_window(x, y, x + 15, y + 15);

    const uint8_t *pfont = longyin_font_data[index];
    uint8_t temp = 0, t1 = 0;
    uint16_t colortemp = 0;

    for (uint8_t t = 0; t < 32; t += 2) {
        temp = pfont[t] << 8 | pfont[t + 1];
        for (t1 = 0; t1 < 16; t1++) {
            if (temp & 0x8000) {
                colortemp = color;
            } else {
                colortemp = BLACK;
            }
            lcd_write_data16(colortemp);
            temp <<= 1;
        }
    }
}

static void lcd_show_chinese_string(uint16_t x, uint16_t y, uint16_t *str, uint16_t color)
{
    uint16_t x0 = x;
    while (*str != 0) {
        if (x >= LCD_WIDTH - 15) {
            x = x0;
            y += 16;
        }
        if (y >= LCD_HEIGHT) break;
        lcd_show_chinese_char(x, y, *str, color);
        x += 16;
        str++;
    }
}

static void lcd_init(void)
{
    gpio_config_t gpio_init_struct;

    spi_device_interface_config_t devcfg = {
        .clock_speed_hz = 60 * 1000 * 1000,
        .mode = 0,
        .spics_io_num = EXAMPLE_PIN_NUM_LCD_CS,
        .queue_size = 7,
    };

    ESP_ERROR_CHECK(spi_bus_add_device(LCD_HOST, &devcfg, &lcd_spi_handle));

    gpio_init_struct.intr_type = GPIO_INTR_DISABLE;
    gpio_init_struct.mode = GPIO_MODE_OUTPUT;
    gpio_init_struct.pin_bit_mask = 1ULL << EXAMPLE_PIN_NUM_LCD_DC;
    gpio_init_struct.pull_down_en = GPIO_PULLDOWN_DISABLE;
    gpio_init_struct.pull_up_en = GPIO_PULLUP_ENABLE;
    gpio_config(&gpio_init_struct);

    gpio_init_struct.pin_bit_mask = 1ULL << EXAMPLE_PIN_NUM_LCD_RST;
    gpio_config(&gpio_init_struct);

    xl9555_pin_write(SLCD_PWR_IO, 1);
    vTaskDelay(pdMS_TO_TICKS(10));

    lcd_hard_reset();

    uint8_t cmd_data[1];

    lcd_write_cmd(0x11);
    vTaskDelay(pdMS_TO_TICKS(120));

    cmd_data[0] = 0xA0;
    lcd_write_cmd(0x36);
    lcd_write_data(cmd_data, 1);

    cmd_data[0] = 0x65;
    lcd_write_cmd(0x3A);
    lcd_write_data(cmd_data, 1);

    lcd_write_cmd(0x21);
    vTaskDelay(pdMS_TO_TICKS(120));

    lcd_write_cmd(0x29);
    vTaskDelay(pdMS_TO_TICKS(120));
}

