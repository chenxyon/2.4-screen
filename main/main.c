static void lcd_show_char(uint16_t x, uint16_t y, uint8_t chr, uint8_t size, uint16_t color)
{
    uint8_t temp = 0, t1 = 0;
    uint8_t *pfont = 0;
    uint8_t csize = 0;
    uint16_t colortemp = 0;

    csize = (size / 8 + ((size % 8) ? 1 : 0)) * (size / 2);
    chr = chr - ' ';

    if ((x > (LCD_WIDTH - size / 2)) || (y > (LCD_HEIGHT - size))) {
        return;
    }

    lcd_set_window(x, y, x + size / 2 - 1, y + size - 1);

    switch (size) {
        case 16:
            pfont = (uint8_t *)asc2_1608[chr];
            break;
        default:
            return;
    }

    csize = (size / 8 + ((size % 8) ? 1 : 0)) * (size / 2);

    for (uint8_t t = 0; t < csize; t++) {
        temp = pfont[t];
        for (t1 = 0; t1 < 8; t1++) {
            if (temp & 0x80) {
                colortemp = color;
            } else {
                colortemp = BLACK;
            }
            lcd_write_data16(colortemp);
            temp <<= 1;
        }
    }
}

static void lcd_show_string(uint16_t x, uint16_t y, uint8_t size, char *p, uint16_t color)
{
    uint8_t x0 = x;
    while ((*p <= '~') && (*p >= ' ')) {
        if (x >= LCD_WIDTH) {
            x = x0;
            y += size;
        }
        if (y >= LCD_HEIGHT) break;
        lcd_show_char(x, y, *p, size, color);
        x += size / 2;
        p++;
    }
}

static void lcd_init(void)
{
    gpio_config_t gpio_init_struct;

    spi_device_interface_config_t devcfg = {
        .clock_speed_hz = 60 * 1000 * 1000,
        .mode = 0,
        .spics_io_num = EXAMPLE_PIN_NUM_LCD_CS,
        .queue_size = 7,
    };

    ESP_ERROR_CHECK(spi_bus_add_device(LCD_HOST, &devcfg, &lcd_spi_handle));

    gpio_init_struct.intr_type = GPIO_INTR_DISABLE;
    gpio_init_struct.mode = GPIO_MODE_OUTPUT;
    gpio_init_struct.pin_bit_mask = 1ULL << EXAMPLE_PIN_NUM_LCD_DC;
    gpio_init_struct.pull_down_en = GPIO_PULLDOWN_DISABLE;
    gpio_init_struct.pull_up_en = GPIO_PULLUP_ENABLE;
    gpio_config(&gpio_init_struct);

    gpio_init_struct.pin_bit_mask = 1ULL << EXAMPLE_PIN_NUM_LCD_RST;
    gpio_config(&gpio_init_struct);

    xl9555_pin_write(SLCD_PWR_IO, 1);
    vTaskDelay(pdMS_TO_TICKS(10));

    lcd_hard_reset();

    uint8_t cmd_data[1];

    lcd_write_cmd(0x11);
    vTaskDelay(pdMS_TO_TICKS(120));

    cmd_data[0] = 0xA0;
    lcd_write_cmd(0x36);
    lcd_write_data(cmd_data, 1);

    cmd_data[0] = 0x65;
    lcd_write_cmd(0x3A);
    lcd_write_data(cmd_data, 1);

    lcd_write_cmd(0x21);
    vTaskDelay(pdMS_TO_TICKS(120));

    lcd_write_cmd(0x29);
    vTaskDelay(pdMS_TO_TICKS(120));
}

static void tft_test_task(void *arg)
{
    ESP_LOGI(TAG, "TFT测试任务开始");

    ESP_LOGI(TAG, "初始化I2C总线...");
    i2c_init();

    ESP_LOGI(TAG, "初始化XL9555 IO扩展芯片...");
    uint8_t data[2] = {0x00, 0x00};
    xl9555_write_byte(XL9555_CONFIG_PORT0_REG, data, 2);
    xl9555_write_byte(XL9555_OUTPUT_PORT0_REG, data, 2);
    vTaskDelay(pdMS_TO_TICKS(10));

    ESP_LOGI(TAG, "打开背光...");
    xl9555_pin_write(LCD_BL_IO, 1);

    ESP_LOGI(TAG, "初始化SPI总线...");
    spi_bus_config_t buscfg = {
        .mosi_io_num = EXAMPLE_PIN_NUM_MOSI,
        .miso_io_num = EXAMPLE_PIN_NUM_MISO,
        .sclk_io_num = EXAMPLE_PIN_NUM_SCLK,
        .quadwp_io_num = -1,
        .quadhd_io_num = -1,
        .max_transfer_sz = LCD_WIDTH * LCD_HEIGHT * 2,
    };
    ESP_ERROR_CHECK(spi_bus_initialize(LCD_HOST, &buscfg, SPI_DMA_CH_AUTO));

    ESP_LOGI(TAG, "初始化ILI9341面板...");
    lcd_init();

    ESP_LOGI(TAG, "测试纯色填充(白色)...");
    lcd_clear(WHITE);
    vTaskDelay(pdMS_TO_TICKS(1000));

    ESP_LOGI(TAG, "测试纯色填充(红色)...");
    lcd_clear(RED);
    vTaskDelay(pdMS_TO_TICKS(1000));

    ESP_LOGI(TAG, "测试纯色填充(绿色)...");
    lcd_clear(GREEN);
    vTaskDelay(pdMS_TO_TICKS(1000));

    ESP_LOGI(TAG, "测试纯色填充(蓝色)...");
    lcd_clear(BLUE);
    vTaskDelay(pdMS_TO_TICKS(1000));

    ESP_LOGI(TAG, "测试显示文字...");
    lcd_clear(WHITE);

    lcd_show_string(50, 50, 16, "Hello World!", RED);
    lcd_show_string(50, 80, 16, "2.4\" TFT LCD Test", BLUE);
    lcd_show_string(50, 110, 16, "Version: ", GREEN);
    lcd_show_string(130, 110, 16, "1.0.0", BLACK);

    lcd_show_string(50, 160, 16, "ABCDEFGHIJKLMNOP", BLACK);
    lcd_show_string(50, 180, 16, "0123456789!@#$%", BLACK);
    lcd_show_string(50, 200, 16, "ZXCVBNM<>?:", BLACK);

    ESP_LOGI(TAG, "测试完成");

    while (1) {
        vTaskDelay(pdMS_TO_TICKS(1000));
    }
}

